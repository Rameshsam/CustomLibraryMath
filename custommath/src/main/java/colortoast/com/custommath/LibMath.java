package colortoast.com.custommath;

public class LibMath {

    public static int add(int a,int b)
    {
        return a+b;
    }


    public static int subtract(int a,int b)
    {
        return a-b;
    }


    public static int multiply(int a,int b)
    {
        return a*b;
    }


    public static int devide(int a,int b)
    {
        return a%b;
    }

}
